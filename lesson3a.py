# Comparison operators- They are used to compare two or more statements and they are used to return a boolean answer.

number1 = 2
number2 = 5

print("is number1 greater than number2? ", number1 > number2)


print("is number1 lesser than number2? ", number1 < number2)


print("is number1 greater than or equal to number2? ", number1 >= number2)


print("is number1 lesser than or equal to number2? ", number1 <= number2)
